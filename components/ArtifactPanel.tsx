function escapeHtml(s: string): string {
  return (s ?? '').replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

function downloadFile(content: string, filename: string, mime: string): void {
  const blob = new Blob([content], { type: mime });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url; a.download = filename;
  document.body.appendChild(a); a.click();
  document.body.removeChild(a);
  setTimeout(() => URL.revokeObjectURL(url), 1000);
}

// ── PDF Export ────────────────────────────────────────────────────
async function exportAsPDF(content: string, title: string, language: string): Promise<void> {
  const { jsPDF } = await import('jspdf');
  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
  const pageW  = doc.internal.pageSize.getWidth();
  const pageH  = doc.internal.pageSize.getHeight();
  const margin = 14;
  const usableW = pageW - margin * 2;

  doc.setFillColor(11, 15, 26);
  doc.rect(0, 0, pageW, 18, 'F');
  doc.setTextColor(16, 185, 129);
  doc.setFontSize(10);
  doc.setFont('helvetica', 'bold');
  doc.text(title, margin, 12);
  doc.setTextColor(100, 120, 140);
  doc.setFontSize(8);
  doc.setFont('helvetica', 'normal');
  doc.text(language.toUpperCase(), pageW - margin, 12, { align: 'right' });

  doc.setTextColor(40, 40, 40);
  doc.setFontSize(8.5);
  doc.setFont('courier', 'normal');
  const lines = doc.splitTextToSize(content, usableW);
  const lineH = 5;
  let y = 26;

  for (const line of lines) {
    if (y + lineH > pageH - margin) {
      doc.addPage();
      doc.setFillColor(11, 15, 26);
      doc.rect(0, 0, pageW, 10, 'F');
      doc.setTextColor(80, 100, 120);
      doc.setFontSize(7);
      doc.setFont('helvetica', 'normal');
      doc.text(`${title} (cont.)`, margin, 7);
      doc.setTextColor(40, 40, 40);
      doc.setFontSize(8.5);
      doc.setFont('courier', 'normal');
      y = 16;
    }
    doc.text(line, margin, y);
    y += lineH;
  }

  const pageCount = (doc.internal as any).getNumberOfPages?.() ?? 1;
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    doc.setTextColor(120, 120, 120);
    doc.setFontSize(7);
    doc.setFont('helvetica', 'normal');
    doc.text(`Generated by Sedrex AI  •  Page ${i} of ${pageCount}`, pageW / 2, pageH - 6, { align: 'center' });
  }
  doc.save(`${title.replace(/[^a-z0-9_\-]/gi, '_')}.pdf`);
}

// ── DOCX Export ───────────────────────────────────────────────────
async function exportAsDOCX(content: string, title: string, language: string): Promise<void> {
  const { Document, Packer, Paragraph, TextRun, HeadingLevel, AlignmentType } = await import('docx');
  const lines = content.split('\n');

  const wordDoc = new Document({
    sections: [{
      properties: {},
      children: [
        new Paragraph({
          heading: HeadingLevel.HEADING_1,
          children: [new TextRun({ text: title, bold: true, size: 28 })],
        }),
        new Paragraph({
          children: [new TextRun({ text: `Language: ${language.toUpperCase()}   |   Generated by Sedrex AI`, italics: true, color: '888888', size: 18 })],
        }),
        new Paragraph({ children: [new TextRun({ text: '' })] }),
        ...lines.map(line =>
          new Paragraph({
            alignment: AlignmentType.LEFT,
            children: [new TextRun({ text: line.length > 0 ? line : ' ', font: 'Courier New', size: 18, color: '1a1a2e' })],
          })
        ),
      ],
    }],
  });

  const blob = await Packer.toBlob(wordDoc);
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `${title.replace(/[^a-z0-9_\-]/gi, '_')}.docx`;
  document.body.appendChild(a); a.click();
  document.body.removeChild(a);
  setTimeout(() => URL.revokeObjectURL(url), 1000);
}

