type ExportFormat = 'source' | 'pdf' | 'docx';

interface ExportMenuProps {
  artifact:  Artifact;
  content:   string;
  onExport:  (format: ExportFormat) => Promise<void>;
}

const ExportMenu: React.FC<ExportMenuProps> = ({ artifact, content, onExport }) => {
  const [open,      setOpen]      = useState(false);
  const [exporting, setExporting] = useState<ExportFormat | null>(null);
  const [dropPos,   setDropPos]   = useState({ top: 0, left: 0 });
  const btnRef  = useRef<HTMLButtonElement>(null);
  const dropRef = useRef<HTMLDivElement>(null);

  // Recompute portal position whenever menu opens
  useEffect(() => {
    if (!open || !btnRef.current) return;
    const rect = btnRef.current.getBoundingClientRect();
    const dropH = dropRef.current?.offsetHeight ?? 120;
    setDropPos({
      top:  Math.max(8, rect.top - dropH - 6),
      left: Math.max(8, rect.right - 170),
    });
  }, [open]);

  // Close on outside click
  useEffect(() => {
    if (!open) return;
    const h = (e: MouseEvent) => {
      if (
        dropRef.current && !dropRef.current.contains(e.target as Node) &&
        btnRef.current  && !btnRef.current.contains(e.target as Node)
      ) setOpen(false);
    };
    const t = setTimeout(() => document.addEventListener('mousedown', h), 10);
    return () => { clearTimeout(t); document.removeEventListener('mousedown', h); };
  }, [open]);

  const handle = async (fmt: ExportFormat) => {
    setOpen(false);
    setExporting(fmt);
    try { await onExport(fmt); } finally { setExporting(null); }
  };

  const items: { id: ExportFormat; label: string; icon: string }[] = [
    { id: 'source', label: 'Source file',    icon: '📄' },
    { id: 'pdf',    label: 'Export as PDF',  icon: '📕' },
    { id: 'docx',   label: 'Export as DOCX', icon: '📘' },
  ];

  // Portal renders into document.body — escapes ap-code-viewer overflow:hidden
  const dropdown = open ? createPortal(
    <div ref={dropRef} style={{
      position: 'fixed', top: dropPos.top, left: dropPos.left,
      background: 'var(--bg-secondary, #0d1117)',
      border: '1px solid var(--border, rgba(255,255,255,0.12))',
      borderRadius: 10, boxShadow: '0 8px 32px rgba(0,0,0,0.6)',
      minWidth: 170, zIndex: 99999, overflow: 'hidden',
    }}>
      {items.map(it => (
        <button key={it.id} onClick={() => handle(it.id)} style={{
          display: 'flex', alignItems: 'center', gap: 8,
          width: '100%', padding: '9px 14px', border: 'none',
          background: 'transparent', color: 'var(--text-primary)',
          cursor: 'pointer', fontSize: 13, textAlign: 'left',
        }}
          onMouseEnter={e => (e.currentTarget.style.background = 'rgba(16,185,129,0.08)')}
          onMouseLeave={e => (e.currentTarget.style.background = 'transparent')}
        >
          <span style={{ fontSize: 14 }}>{it.icon}</span>{it.label}
        </button>
      ))}
    </div>,
    document.body,
  ) : null;

  return (
    <>
      <button
        ref={btnRef}
        className="ap-action-btn"
        onClick={() => setOpen(v => !v)}
        disabled={!!exporting}
        title="Export"
      >
        {exporting
          ? <span className="ap-spinner" style={{ width: 10, height: 10 }} />
          : <Icons.Download className="icon-12" />}
        <span>{exporting ? 'Exporting…' : 'Export ▾'}</span>
      </button>
      {dropdown}
    </>
  );
};

